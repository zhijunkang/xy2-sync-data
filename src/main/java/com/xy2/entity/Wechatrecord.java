package com.xy2.entity;


public class Wechatrecord {

  private String chatId;
  private String chatMes;
  private String chatSendid;
  private String chatGetid;
  private String time;


  public String getChatId() {
    return chatId;
  }

  public void setChatId(String chatId) {
    this.chatId = chatId;
  }


  public String getChatMes() {
    return chatMes;
  }

  public void setChatMes(String chatMes) {
    this.chatMes = chatMes;
  }


  public String getChatSendid() {
    return chatSendid;
  }

  public void setChatSendid(String chatSendid) {
    this.chatSendid = chatSendid;
  }


  public String getChatGetid() {
    return chatGetid;
  }

  public void setChatGetid(String chatGetid) {
    this.chatGetid = chatGetid;
  }


  public String getTime() {
    return time;
  }

  public void setTime(String time) {
    this.time = time;
  }

}
