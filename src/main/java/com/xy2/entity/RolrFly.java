package com.xy2.entity;


public class RolrFly {

  private String flyId;
  private String flyName;
  private String flyLevel;
  private String roleId;
  private String moveLevel;
  private String fuel;
  private String skin;


  public String getFlyId() {
    return flyId;
  }

  public void setFlyId(String flyId) {
    this.flyId = flyId;
  }


  public String getFlyName() {
    return flyName;
  }

  public void setFlyName(String flyName) {
    this.flyName = flyName;
  }


  public String getFlyLevel() {
    return flyLevel;
  }

  public void setFlyLevel(String flyLevel) {
    this.flyLevel = flyLevel;
  }


  public String getRoleId() {
    return roleId;
  }

  public void setRoleId(String roleId) {
    this.roleId = roleId;
  }


  public String getMoveLevel() {
    return moveLevel;
  }

  public void setMoveLevel(String moveLevel) {
    this.moveLevel = moveLevel;
  }


  public String getFuel() {
    return fuel;
  }

  public void setFuel(String fuel) {
    this.fuel = fuel;
  }


  public String getSkin() {
    return skin;
  }

  public void setSkin(String skin) {
    this.skin = skin;
  }

}
