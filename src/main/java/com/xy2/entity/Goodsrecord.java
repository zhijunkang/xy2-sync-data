package com.xy2.entity;


public class Goodsrecord {

  private String grid;
  private String recordtype;
  private String roleid;
  private String otherrole;
  private String goods;
  private java.sql.Date recordtime;
  private String goodsnum;
  private String rolename;
  private String othername;


  public String getGrid() {
    return grid;
  }

  public void setGrid(String grid) {
    this.grid = grid;
  }


  public String getRecordtype() {
    return recordtype;
  }

  public void setRecordtype(String recordtype) {
    this.recordtype = recordtype;
  }


  public String getRoleid() {
    return roleid;
  }

  public void setRoleid(String roleid) {
    this.roleid = roleid;
  }


  public String getOtherrole() {
    return otherrole;
  }

  public void setOtherrole(String otherrole) {
    this.otherrole = otherrole;
  }


  public String getGoods() {
    return goods;
  }

  public void setGoods(String goods) {
    this.goods = goods;
  }


  public java.sql.Date getRecordtime() {
    return recordtime;
  }

  public void setRecordtime(java.sql.Date recordtime) {
    this.recordtime = recordtime;
  }


  public String getGoodsnum() {
    return goodsnum;
  }

  public void setGoodsnum(String goodsnum) {
    this.goodsnum = goodsnum;
  }


  public String getRolename() {
    return rolename;
  }

  public void setRolename(String rolename) {
    this.rolename = rolename;
  }


  public String getOthername() {
    return othername;
  }

  public void setOthername(String othername) {
    this.othername = othername;
  }

}
