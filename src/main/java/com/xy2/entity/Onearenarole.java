package com.xy2.entity;


public class Onearenarole {

  private String roleid;
  private String place;
  private String skin;
  private String name;
  private String lvl;
  private String isaward;
  private String placepast;


  public String getRoleid() {
    return roleid;
  }

  public void setRoleid(String roleid) {
    this.roleid = roleid;
  }


  public String getPlace() {
    return place;
  }

  public void setPlace(String place) {
    this.place = place;
  }


  public String getSkin() {
    return skin;
  }

  public void setSkin(String skin) {
    this.skin = skin;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public String getLvl() {
    return lvl;
  }

  public void setLvl(String lvl) {
    this.lvl = lvl;
  }


  public String getIsaward() {
    return isaward;
  }

  public void setIsaward(String isaward) {
    this.isaward = isaward;
  }


  public String getPlacepast() {
    return placepast;
  }

  public void setPlacepast(String placepast) {
    this.placepast = placepast;
  }

}
