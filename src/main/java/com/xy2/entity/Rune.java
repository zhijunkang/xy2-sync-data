package com.xy2.entity;


public class Rune {

  private String runeid;
  private String runename;
  private String runeskin;
  private String runetype;
  private String runequality;
  private String runevalue;
  private String runetext;


  public String getRuneid() {
    return runeid;
  }

  public void setRuneid(String runeid) {
    this.runeid = runeid;
  }


  public String getRunename() {
    return runename;
  }

  public void setRunename(String runename) {
    this.runename = runename;
  }


  public String getRuneskin() {
    return runeskin;
  }

  public void setRuneskin(String runeskin) {
    this.runeskin = runeskin;
  }


  public String getRunetype() {
    return runetype;
  }

  public void setRunetype(String runetype) {
    this.runetype = runetype;
  }


  public String getRunequality() {
    return runequality;
  }

  public void setRunequality(String runequality) {
    this.runequality = runequality;
  }


  public String getRunevalue() {
    return runevalue;
  }

  public void setRunevalue(String runevalue) {
    this.runevalue = runevalue;
  }


  public String getRunetext() {
    return runetext;
  }

  public void setRunetext(String runetext) {
    this.runetext = runetext;
  }

}
